    private void hiitungbuttonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_hiitungbuttonActionPerformed
    String nama = textnama.getText();
                int golongan = 0;
                if (golongan1RadioButton.isSelected()) {
                    golongan = 1;
                } else if (golongan2RadioButton.isSelected()) {
                    golongan = 2;
                } else if (golongan3RadioButton.isSelected()) {
                    golongan = 3;
                }
                int lamaKerja = Integer.parseInt(textlama.getText());

                double gajiPokok = hitungGajiPokok(golongan);
                double tunjangan = hitungTunjangan(golongan, lamaKerja);
                double totalGaji = gajiPokok + tunjangan;
                String bonus = tunjangan + "\n";
                textunjangan.setText(bonus);
                Tbdata.setValueAt(textnama.getText(), x, 0);
                Tbdata.setValueAt(textnik.getText(), x, 1);
                String tanggal = Tanggal.getSelectedItem().toString();
                String bulan = Bulan.getSelectedItem().toString();
                String tahun = Tahun.getSelectedItem().toString();
                String tanggalLahir = tanggal + " " + bulan + " " + tahun;
                Tbdata.setValueAt(tanggalLahir, x, 2);
                Tbdata.setValueAt(textlama.getText(), x, 3);
                Tbdata.setValueAt(totalGaji, x, 4);
                x=x+1;

    
    }//GEN-LAST:event_hiitungbuttonActionPerformed

