private double hitungGajiPokok(int golongan) {
        double gajiPokok = 0;
        switch (golongan) {
            case 1:
                gajiPokok = 5000000;
                break;
            case 2:
                gajiPokok = 7000000;
                break;
            case 3:
                gajiPokok = 9000000;
                break;
        }
        return gajiPokok;
    }
private double hitungTunjangan(int golongan, int lamaKerja) {
        double tunjangan = 0;
        if (lamaKerja >= 12) {
            switch (golongan) {
                case 1:
                    tunjangan = 0.15 * hitungGajiPokok(golongan);
                    break;
                case 2:
                    tunjangan = 0.2 * hitungGajiPokok(golongan);
                    break;
                case 3:
                    tunjangan = 0.25 * hitungGajiPokok(golongan);
                    break;
            }
        }
        return tunjangan;
    }
 

